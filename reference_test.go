package fpc

import (
	"reflect"
	"testing"
)

type refinput struct {
	comp uint
	in   []float64
}

type reftestcase struct {
	comp         uint
	uncompressed []float64
	compressed   []byte
}

func (tc reftestcase) idx() int {
	for i, rtc := range refTests {
		if reflect.DeepEqual(rtc, tc) {
			return i
		}
	}
	return -1
}

func (tc reftestcase) AssertNoError(t *testing.T, err error, msg string) {
	if err != nil {
		tc.Error(t, msg, err)
	}
}

func (tc reftestcase) AssertEqual(t *testing.T, have, want interface{}, msg string) {
	if !reflect.DeepEqual(have, want) {
		t.Errorf("%s  test=%d", msg, tc.idx())
		t.Logf("  comp=%#v", tc.compressed)
		t.Logf("uncomp=%#v", tc.uncompressed)
		t.Logf("  have=%#v", have)
		t.Logf("  want=%#v", want)
	}
}

func (tc reftestcase) Fail(t *testing.T, msg string, have interface{}) {
}

func (tc reftestcase) Error(t *testing.T, msg string, err error) {
	t.Errorf("%s  test=%d  err=%q", msg, tc.idx(), err)
	t.Logf("  comp=%#v", tc.compressed)
	t.Logf("uncomp=%#v", tc.uncompressed)
}

